namespace DualDrill.ApiGen;

internal static class IndentedTextWriterExtension
{
    public static void WriteStructLayout(this TextWriter writer)
    {
        writer.WriteLine("[StructLayout(LayoutKind.Sequential)]");
    }
    public static void WriteAggressiveInlining(this TextWriter writer)
    {
        writer.WriteLine("[MethodImpl(MethodImplOptions.AggressiveInlining)]");
    }

    public static void WriteGeneratedCodeComment(this TextWriter writer)
    {
        writer.WriteLine("//------------------------------------------------------------------------------");
        writer.WriteLine("// <auto-generated>");
        writer.WriteLine("//     This code was generated by a tool.");
        writer.WriteLine("//");
        writer.WriteLine("//     Changes to this file may cause incorrect behavior and will be lost if");
        writer.WriteLine("//     the code is regenerated.");
        writer.WriteLine("// </auto-generated>");
        writer.WriteLine("//------------------------------------------------------------------------------");
    }
}

